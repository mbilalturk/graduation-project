    "# 8. Deep Learning (LSTM)\n",
    "try:\n",
    "    checkpoint = torch.load(os.path.join(MODELS_DIR, 'improved_lstm.pt'), map_location='cpu')\n",
    "    \n",
    "    class ImprovedLSTM(nn.Module):\n",
    "        def __init__(self, n_seq_feats, n_ctx_feats, rnn_units=128, dropout=0.2):\n",
    "            super().__init__()\n",
    "            self.rnn      = nn.LSTM(n_seq_feats, rnn_units, num_layers=2, batch_first=True, dropout=dropout)\n",
    "            self.rnn_drop = nn.Dropout(dropout)\n",
    "            self.ctx_fc   = nn.Linear(n_ctx_feats, 32)\n",
    "            self.ctx_relu = nn.ReLU()\n",
    "            self.ctx_drop = nn.Dropout(dropout)\n",
    "            self.fc1      = nn.Linear(rnn_units + 32, 64)\n",
